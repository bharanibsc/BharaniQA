package org.sample;

public class DataTypes {

	public static void main(String[] args) {

                   //Datatype Variable =Value;
		
		byte b=10;               //declare a byte variable and assign the value i.e 10
		System.out.println(b);    // print the value of byte variable b

		short s1=30;
		short s2=40;
		System.out.println("the number is :"+s1);
		System.out.println("the number is :"+s2);
		
		int num1=10;
		int num2=20;
		System.out.println(num1);
		System.out.println(num2);
		
		long size1=40;
		long size2=50;
		System.out.println(size1);
		System.out.println(size2);
		
		float mg=30.44839938434f;
		System.out.println(mg);
		
		double pg=25.847658389493;
		System.out.println(pg);
		
		char d='a';
		char e='w';
		System.out.println(d);
		System.out.println(e);

		boolean a=true;
		boolean c=false;
		System.out.println(a);
		System.out.println(c);
		
		String phone ="98219121202";
		System.out.println(phone);
		
	}

}
